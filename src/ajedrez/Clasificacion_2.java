    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jugar_interfaz jug=new jugar_interfaz();
        tablero tab=new tablero();
        boolean resuelto = false;
        String user = this.jLabel5.getText();
        estadisticas_problemas ventana=new estadisticas_problemas();
        int i = Integer.parseInt(this.jLabel2.getText());
        if (!ventana.estaresuelto(resuelto,i,user)){
            resuelto=false;
            jug.setVisible(true);
            this.setEnabled(true);
            this.setVisible(false);
            jug.jLabel1.setText(this.jLabel5.getText());
            jug.jTextArea1.setText(tab.pintartablero(i));
            jug.jTextArea2.setText(tab.pintarPosiciones());
            jug.jLabel5.setText(""+i);
            jug.jLabel8.setText(""+resuelto);
            jug.jLabel9.setText("Este problema contara para las estadisticas");
        }else{
            resuelto=true;
            jug.setVisible(true);
            this.setEnabled(true);
            this.setVisible(false);
            jug.jLabel1.setText(this.jLabel5.getText());
            jug.jTextArea1.setText(tab.pintartablero(i));
            jug.jTextArea2.setText(tab.pintarPosiciones());
            jug.jLabel5.setText(""+i);   
            jug.jLabel8.setText(""+resuelto);
            jug.jLabel9.setText("Este problema no contara para las estadisticas");
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

